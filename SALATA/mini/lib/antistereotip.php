<p><b>Član 2.</b></p>
<p><span>Detaljno iskazani ciljevi i načini ostvarivanja ciljeva Udruženja su:</span></p>
<li>upućivanje i edukovanje u osnovne strukture računarskih sistema kao i rad sa 
osnovnim alatima neophodnim za savremeno poslovanje nezavisno od vrste 
posla;</li>
<li>edukovanje u oblasti programiranja – primenjeno matematičko područje;</li>
<li>edukacija i osposobljavanje za rad sa alatima za programiranje i programskim jezicima;</li>
<li>edukacija u oblasti sigurnosti dece na internetu;</li>
<li>organizovanje različitih humanitarnih i volonterskih akcija;</li>
<li>rad na unapređenju informatičke struke praćenjem aktuelnih trendova i nove tehnologije;</li>
<li>održavanje i organizovanje kurseva, seminara, takmičenja i stručnih smotri;</li>
<li>organizovanje druženje radi širenja znanja i unapređenja međuljudskih odnosa;</li>
<li>saradnja sa drugim klubovima, udruženjima, obrazovnim institucijama i organizacijama;</li>
<li>prikupljanje i obrada naučne i stručne literature u oblasti računarstva;</li>
<li>prikupljanje i obrada statističkih podataka relevantnih za stanje informacionog društva;</li>
<li>predikciona analiza;</li>
<li>učestvovanje u realizaciji strategije razvoja informacionog društva;</li>
<li>sprovođenje društveno odgovornih projekata u različitim oblastima;</li>
<li>objavljivanje biltena, časopisa i druge literature sa informatičkom tematikom;</li>
<li>rad sa marginalizovanim grupama stanovništva i pomoć zavisnicima;</li>
<li>prevencija širenja negativnih društvenih pojava, zavisnosti od poroka i društveno neprihvatljivih oblika ponašanja;</li>
<li>prevencija, pomoć i zaštita oblicima dečije pornografije (pedofilije) i trafikinga (trgovina ljudima);</li>
