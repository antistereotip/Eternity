<p><b>Član 1.</b></p>
<p>
<span>Udruženje građana za besplatnu informatičku edukaciju i društveno odgovorne projekte</span> Antistereotip 
(u daljem tekstu: Udruženje) je dobrovoljno, nevladino i neprofitabilno, nestranačko udruženje, osnovano na neodređeno vreme radi 
ostvarivanja ciljeva u oblasti besplatne informatičke edukacije socijalno ugrožene omladine i dece, sprovođenja društveno odgovornih 
projekata, rada sa <em>marginalizovanim grupama stanovništva i pomoći zavisnicima, kao i žrtvama trafiking-a.</em>
</p>
